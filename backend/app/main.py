@app.get("/api/weather/{lat}/{lon}")
async def get_weather(lat: float, lon: float):
    try:
        async with httpx.AsyncClient(timeout=8) as client:
            r = await client.get(f"https://marine-api.open-meteo.com/v1/marine?latitude={lat}&longitude={lon}&hourly=wave_height,wave_direction,wave_period&forecast_days=1")
            d = r.json()
            wh = d.get("hourly",{}).get("wave_height",[0]*24)
            avg = round(sum(wh[:6])/6,2) if wh else 0
            return {"lat":lat,"lon":lon,"avg_wave_height_m":avg,"risk":"HIGH" if avg>3 else "MEDIUM" if avg>1.5 else "LOW","source":"open-meteo"}
    except: return {"lat":lat,"lon":lon,"avg_wave_height_m":1.2,"risk":"LOW","source":"fallback"}

