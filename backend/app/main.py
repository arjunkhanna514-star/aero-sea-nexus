assets = {v["id"]: dict(v) for v in VESSELS}
alerts = []

def risk_score(lat, lon, speed, asset_type):
    base = random.uniform(20, 80)
    if asset_type == "vessel" and abs(lat) > 45: base += 20
    if speed > 800: base += 10
    return min(round(base, 1), 99)

def move_asset(a):
    r = math.radians(a["heading"])
    spd = a["speed"]
    factor = 0.01 if a["type"]=="flight" else 0.001
    a["lat"] = round(a["lat"] + math.cos(r)*spd*factor*random.uniform(0.8,1.2), 4)
    a["lon"] = round(a["lon"] + math.sin(r)*spd*factor*random.uniform(0.8,1.2), 4)
    a["heading"] = (a["heading"] + random.randint(-3,3)) % 360
    a["risk"] = risk_score(a["lat"], a["lon"], a["speed"], a["type"])
    a["updated"] = datetime.datetime.utcnow().isoformat()
    return a

@app.on_event("startup")
async def startup():
    for a in assets.values(): move_asset(a)
    asyncio.create_task(refresh_loop())

async def refresh_loop():
    while True:
        await asyncio.sleep(int(os.getenv("REFRESH_INTERVAL","30")))
        for a in assets.values(): move_asset(a)
        check_alerts()

def check_alerts():
    for a in assets.values():
        if a.get("risk",0) > 70:
            alerts.append({"id":a["id"]+"_"+datetime.datetime.utcnow().isoformat()[:19],
                "asset":a["name"],"type":a["type"],"risk":a["risk"],
                "msg":f"{a["name"]} risk {a["risk"]}% on {a["route"]}",
                "time":datetime.datetime.utcnow().isoformat()})
    while len(alerts) > 50: alerts.pop(0)

